import { describe, expect, test, it } from "bun:test"
import { base64Decode, base64Encode, checksum } from "../src/encode"

describe("encode utilities", () => {
  describe("base64Decode", () => {
    it("should decode standard base64", () => {
      expect(base64Decode("aGVsbG8=")).toBe("hello")
      expect(base64Decode("aGVsbG8gd29ybGQ=")).toBe("hello world")

      // "hello world?!" encodes to "aGVsbG8gd29ybGQ/IQ==" in standard base64 (contains /)
      expect(base64Decode("aGVsbG8gd29ybGQ/IQ==")).toBe("hello world?!")

      // "hello world?!" encodes to "aGVsbG8gd29ybGQ/IQ" without padding
      expect(base64Decode("aGVsbG8gd29ybGQ/IQ")).toBe("hello world?!")
    })

    it("should decode url-safe base64", () => {
      // url-safe replaces + with - and / with _

      // "hello world?!" encodes to "aGVsbG8gd29ybGQ/IQ==" in standard, so "aGVsbG8gd29ybGQ_IQ==" in url-safe
      expect(base64Decode("aGVsbG8gd29ybGQ_IQ==")).toBe("hello world?!")
      expect(base64Decode("aGVsbG8gd29ybGQ_IQ")).toBe("hello world?!") // no padding

      // let's test a character that encodes with + in standard base64
      // "\xFB\xEF\xBF" encodes to "w7vDr8K/" using TextEncoder + standard base64, which is not what we want to test manually here easily.

      // Just test a known payload generated by base64Encode that contains - or _
      // "🚀" encodes to 4 bytes: [240, 159, 154, 128] -> "8J+agA==" (standard)
      // url-safe: "8J-agA" (no padding, with -)
      expect(base64Decode("8J+agA==")).toBe("🚀") // standard
      expect(base64Decode("8J-agA")).toBe("🚀")   // url-safe
    })

    it("should properly decode base64 strings stripped of padding (=)", () => {
      expect(base64Decode("aGVsbG8")).toBe("hello")
      expect(base64Decode("aGVsbG8gd29ybGQ")).toBe("hello world")
    })

    it("should round-trip properly with base64Encode", () => {
      const inputs = [
        "hello",
        "hello world",
        "",
        "🚀",
        "Hello \n World",
        JSON.stringify({ a: 1, b: 2, c: [1, 2, 3] }),
        "?!@#$%^&*()_+{}[]|\\:;\"'<>,./~`",
        "subject:?something=other&foo=bar",
        "\x00\x01\x02\x03", // control chars
      ]

      for (const input of inputs) {
        const encoded = base64Encode(input)

        // Ensure the encoder actually generated URL-safe and padding-less output
        expect(encoded).not.toContain("+")
        expect(encoded).not.toContain("/")
        expect(encoded).not.toContain("=")

        const decoded = base64Decode(encoded)
        expect(decoded).toBe(input)
      }
    })

    it("should decode an empty string to an empty string", () => {
      expect(base64Decode("")).toBe("")
    })
  })

  describe("checksum", () => {
    test("returns undefined for empty strings", () => {
      expect(checksum("")).toBeUndefined()
    })

    test("generates consistent checksums for identical strings", () => {
      const input = "hello world"
      expect(checksum(input)).toBe(checksum(input))
    })

    test("generates different checksums for different strings", () => {
      expect(checksum("hello world")).not.toBe(checksum("hello world!"))
    })

    test("generates different checksums for same strings with different casing", () => {
      expect(checksum("hello world")).not.toBe(checksum("Hello world"))
    })

    test("generates expected known checksum values", () => {
      // The implementation is a variation of FNV-1a.
      // Let's ensure the output is stable by asserting against a known value.
      expect(checksum("hello world")).toBe("1n91413")
      expect(checksum("The quick brown fox jumps over the lazy dog")).toBe("19kn80")
      expect(checksum("testing123")).toBe("fcbnpx")
    })

    test("handles special characters and unicode", () => {
      // Test that strings with special characters yield a result and are consistent
      const input1 = "こんにちは世界" // Japanese for Hello World
      expect(typeof checksum(input1)).toBe("string")
      expect(checksum(input1)).toBe(checksum(input1))

      const input2 = "🌟🚀" // Emojis
      expect(typeof checksum(input2)).toBe("string")
      expect(checksum(input2)).toBe(checksum(input2))
    })

    test("handles long strings consistently", () => {
      const longString = "a".repeat(10000)
      expect(typeof checksum(longString)).toBe("string")
      expect(checksum(longString)).toBe(checksum(longString))
    })
  })
})
